impl TreelandAppIdResolverV1 {
    /// Since when the identify_request message is available.
    pub const MSG__IDENTIFY_REQUEST__SINCE: u32 = 1;

    /// ask client to resolve pidfd to application id
    ///
    /// # Arguments
    ///
    /// - `request_id`: monotonic id generated by compositor
    /// - `pidfd`: pidfd referencing the target process
    #[inline]
    pub fn try_send_identify_request(
        &self,
        request_id: u32,
        pidfd: &Rc<OwnedFd>,
    ) -> Result<(), ObjectError> {
        let (
            arg0,
            arg1,
        ) = (
            request_id,
            pidfd,
        );
        let core = self.core();
        let client_ref = core.client.borrow();
        let Some(client) = &*client_ref else {
            return Err(ObjectError(ObjectErrorKind::ReceiverNoClient));
        };
        let id = core.client_obj_id.get().unwrap_or(0);
        if self.core.state.log {
            #[cold]
            fn log(state: &State, client_id: u64, id: u32, arg0: u32, arg1: i32) {
                let (millis, micros) = time_since_epoch();
                let prefix = &state.log_prefix;
                let args = format_args!("[{millis:7}.{micros:03}] {prefix}client#{:<4} <= treeland_app_id_resolver_v1#{}.identify_request(request_id: {}, pidfd: {})\n", client_id, id, arg0, arg1);
                state.log(args);
            }
            log(&self.core.state, client.endpoint.id, id, arg0, arg1.as_raw_fd());
        }
        let endpoint = &client.endpoint;
        if !endpoint.flush_queued.replace(true) {
            self.core.state.add_flushable_endpoint(endpoint, Some(client));
        }
        let mut outgoing_ref = endpoint.outgoing.borrow_mut();
        let outgoing = &mut *outgoing_ref;
        let mut fmt = outgoing.formatter();
        fmt.fds.push_back(arg1.clone());
        fmt.words([
            id,
            0,
            arg0,
        ]);
        Ok(())
    }

    /// ask client to resolve pidfd to application id
    ///
    /// # Arguments
    ///
    /// - `request_id`: monotonic id generated by compositor
    /// - `pidfd`: pidfd referencing the target process
    #[inline]
    pub fn send_identify_request(
        &self,
        request_id: u32,
        pidfd: &Rc<OwnedFd>,
    ) {
        let res = self.try_send_identify_request(
            request_id,
            pidfd,
        );
        if let Err(e) = res {
            log_send("treeland_app_id_resolver_v1.identify_request", &e);
        }
    }

    /// Since when the respond message is available.
    pub const MSG__RESPOND__SINCE: u32 = 1;

    /// respond with app id and sandbox_engine_name (empty app_id on failure)
    ///
    /// Respond to an identify_request. The sandbox_engine_name must be provided and
    /// matches the context in which the process is running (container, sandbox, etc).
    /// If resolution fails, respond with an empty app_id.
    ///
    /// # Arguments
    ///
    /// - `request_id`:
    /// - `app_id`:
    /// - `sandbox_engine_name`: the sandbox engine / security context name (required, non-empty)
    #[inline]
    pub fn try_send_respond(
        &self,
        request_id: u32,
        app_id: &str,
        sandbox_engine_name: &str,
    ) -> Result<(), ObjectError> {
        let (
            arg0,
            arg1,
            arg2,
        ) = (
            request_id,
            app_id,
            sandbox_engine_name,
        );
        let core = self.core();
        let Some(id) = core.server_obj_id.get() else {
            return Err(ObjectError(ObjectErrorKind::ReceiverNoServerId));
        };
        if self.core.state.log {
            #[cold]
            fn log(state: &State, id: u32, arg0: u32, arg1: &str, arg2: &str) {
                let (millis, micros) = time_since_epoch();
                let prefix = &state.log_prefix;
                let args = format_args!("[{millis:7}.{micros:03}] {prefix}server      <= treeland_app_id_resolver_v1#{}.respond(request_id: {}, app_id: {:?}, sandbox_engine_name: {:?})\n", id, arg0, arg1, arg2);
                state.log(args);
            }
            log(&self.core.state, id, arg0, arg1, arg2);
        }
        let endpoint = &self.core.state.server;
        if !endpoint.flush_queued.replace(true) {
            self.core.state.add_flushable_endpoint(endpoint, None);
        }
        let mut outgoing_ref = endpoint.outgoing.borrow_mut();
        let outgoing = &mut *outgoing_ref;
        let mut fmt = outgoing.formatter();
        fmt.words([
            id,
            0,
            arg0,
        ]);
        fmt.string(arg1);
        fmt.string(arg2);
        Ok(())
    }

    /// respond with app id and sandbox_engine_name (empty app_id on failure)
    ///
    /// Respond to an identify_request. The sandbox_engine_name must be provided and
    /// matches the context in which the process is running (container, sandbox, etc).
    /// If resolution fails, respond with an empty app_id.
    ///
    /// # Arguments
    ///
    /// - `request_id`:
    /// - `app_id`:
    /// - `sandbox_engine_name`: the sandbox engine / security context name (required, non-empty)
    #[inline]
    pub fn send_respond(
        &self,
        request_id: u32,
        app_id: &str,
        sandbox_engine_name: &str,
    ) {
        let res = self.try_send_respond(
            request_id,
            app_id,
            sandbox_engine_name,
        );
        if let Err(e) = res {
            log_send("treeland_app_id_resolver_v1.respond", &e);
        }
    }

    /// Since when the destroy message is available.
    pub const MSG__DESTROY__SINCE: u32 = 1;

    /// destroy resolver
    #[inline]
    pub fn try_send_destroy(
        &self,
    ) -> Result<(), ObjectError> {
        let core = self.core();
        let Some(id) = core.server_obj_id.get() else {
            return Err(ObjectError(ObjectErrorKind::ReceiverNoServerId));
        };
        if self.core.state.log {
            #[cold]
            fn log(state: &State, id: u32) {
                let (millis, micros) = time_since_epoch();
                let prefix = &state.log_prefix;
                let args = format_args!("[{millis:7}.{micros:03}] {prefix}server      <= treeland_app_id_resolver_v1#{}.destroy()\n", id);
                state.log(args);
            }
            log(&self.core.state, id);
        }
        let endpoint = &self.core.state.server;
        if !endpoint.flush_queued.replace(true) {
            self.core.state.add_flushable_endpoint(endpoint, None);
        }
        let mut outgoing_ref = endpoint.outgoing.borrow_mut();
        let outgoing = &mut *outgoing_ref;
        let mut fmt = outgoing.formatter();
        fmt.words([
            id,
            1,
        ]);
        self.core.handle_server_destroy();
        Ok(())
    }

    /// destroy resolver
    #[inline]
    pub fn send_destroy(
        &self,
    ) {
        let res = self.try_send_destroy(
        );
        if let Err(e) = res {
            log_send("treeland_app_id_resolver_v1.destroy", &e);
        }
    }
}

